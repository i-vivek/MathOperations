public struct MathOperations {
    
    public init() {}
    
    public func add(value firstValue: Int, to secondValue: Int) -> Int {
        return firstValue + secondValue
    }
    
    public func subtract(value firstValue: Int, to secondValue: Int) -> Int {
        return firstValue - secondValue
    }
    
    public func multiply(value firstValue: Int, to secondValue: Int) -> Int {
        return firstValue * secondValue
    }
    
    public func divide(value firstValue: Int, to secondValue: Int) -> Int {
        return firstValue / secondValue
    }
    
    public func modulo(value firstValue: Int, to secondValue: Int) -> Int {
        return firstValue % secondValue
    }
}
